def human_block(claim: str, source: str, snippet: str) -> str:
    return (
        "Проверь достоверность утверждения.\n\n"
        f"Утверждение: {claim}\n\n"
        "Найденные новости:\n"
        f"1. {claim}\n"
        f"   Источник: {source}\n"
        f"   {snippet}"
    )


def gpt_block_true(claim: str, source: str, snippet: str, dv: int, cv: int) -> str:
    snip_short = snippet[:100]
    return (
        "<reasoning>\n"
        f"Шаг 1 — Идентификация: Проверяю «{claim}».\n"
        f"Нахожу релевантный источник: {source}. Тема и детали полностью совпадают.\n\n"
        f"Шаг 2 — Факты: Из {source}: «{snip_short}».\n"
        "Прямое подтверждение. Участники, хронология и цифры идентичны.\n\n"
        "Шаг 3 — Числовая проверка: Сравниваю числа из утверждения с источником — расхождений нет.\n\n"
        "Шаг 4 — Самопроверка:\n"
        "А) Источник авторитетный, данные конкретные.\n"
        "Б) Совпадение не поверхностное — проверил каждый факт.\n"
        "В) Адвокат дьявола: что если источник ошибается? Маловероятно — данные подтверждены.\n"
        "Г) ПРАВДА — вердикт обоснован.\n"
        "</reasoning>\n"
        "<answer>\n"
        f"ДОСТОВЕРНОСТЬ: {dv}\n"
        "ВЕРДИКТ: ПРАВДА\n"
        f"УВЕРЕННОСТЬ: {cv}\n"
        f"ОБОСНОВАНИЕ: Источник ({source}) подтверждает: «{snip_short}». Данные совпадают с утверждением. Противоречий не обнаружено.\n"
        f"ИСТОЧНИКИ: {source}\n"
        "</answer>"
    )


def gpt_block_false(claim: str, source: str, snippet: str, reality: str, dv: int, cv: int) -> str:
    snip_short = snippet[:120]
    return (
        "<reasoning>\n"
        f"Шаг 1 — Идентификация: Анализирую «{claim}».\n"
        "Утверждение содержит фактическую ошибку — проверяю против источника.\n\n"
        f"Шаг 2 — Факты: {source}: «{snip_short}».\n"
        "Ключевые факты расходятся: утверждение говорит одно, источник — другое.\n"
        f"Реальность: «{reality}».\n\n"
        "Шаг 3 — Числовая проверка: Конкретное расхождение в числах/датах/фактах.\n\n"
        "Шаг 4 — Самопроверка:\n"
        "А) Не путаю «не найдено» с «опровергнуто» — здесь именно опровержение.\n"
        "Б) Источник прямо противоречит утверждению.\n"
        "В) Адвокат дьявола: а если утверждение метафорично? Нет — оно фактическое и неверно.\n"
        "Г) ЛОЖЬ.\n"
        "</reasoning>\n"
        "<answer>\n"
        f"ДОСТОВЕРНОСТЬ: {dv}\n"
        "ВЕРДИКТ: ЛОЖЬ\n"
        f"УВЕРЕННОСТЬ: {cv}\n"
        f"ОБОСНОВАНИЕ: По данным {source}: «{snip_short}». Это ПРОТИВОРЕЧИТ утверждению «{claim}». Реальные данные: «{reality}».\n"
        "ИСТОЧНИКИ: Подтверждающие источники не найдены\n"
        "</answer>"
    )


def make_ex_true(claim: str, source: str, snippet: str, dv=90, cv=88) -> dict:
    return {"conversations": [
        {"from": "human", "value": human_block(claim, source, snippet)},
        {"from": "gpt", "value": gpt_block_true(claim, source, snippet, dv, cv)},
    ]}


def make_ex_false(claim: str, source: str, snippet: str, reality: str, dv=5, cv=85) -> dict:
    return {"conversations": [
        {"from": "human", "value": human_block(claim, source, snippet)},
        {"from": "gpt", "value": gpt_block_false(claim, source, snippet, reality, dv, cv)},
    ]}


