    data = []
    data += gen_myths();          print(f"  Мифы:         {len(data)}")
    data += gen_conspiracies();   print(f"  + Конспир.:   {len(data)}")
    data += gen_swaps();          print(f"  + Swaps:      {len(data)}")
    data += gen_quantifiers();    print(f"  + Квантиф.:   {len(data)}")
    data += gen_directional();    print(f"  + Direction.: {len(data)}")

    random.shuffle(data)
