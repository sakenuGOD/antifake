def rnd_source() -> str:
    return random.choice(SOURCES_AUTHORITATIVE)


def gen_myths() -> list:
    """100 примеров: мифы → ЛОЖЬ (по 5 вариаций каждой из 20 баз)."""
    out = []
    for myth_claim, snippet, reality in MYTHS:
        claim_low = myth_claim[0].lower() + myth_claim[1:]
        variations = [
            myth_claim,
            f"Известно, что {claim_low}",
            f"Научный факт: {claim_low}",
            f"Доказано, что {claim_low}",
            f"{myth_claim} — это подтверждённый факт",
        ]
        for v in variations:
            out.append(make_ex_false(v, rnd_source(), snippet, reality,
                                     dv=random.randint(3, 12),
                                     cv=random.randint(80, 92)))
    return out


def gen_conspiracies() -> list:
    """100 примеров: 10 теорий × 10 формулировок → все ЛОЖЬ."""
    out = []
    for _theory_id, formulations, snippet, reality in CONSPIRACIES_CORE:
        for f in formulations:
            out.append(make_ex_false(f, rnd_source(), snippet, reality,
                                     dv=random.randint(2, 8),
                                     cv=random.randint(82, 94)))
    return out


def gen_swaps() -> list:
    """100 примеров: 25 пар × (TRUE+FALSE) × 2 формулировки каждой."""
    out = []
    for true_claim, false_claim, snippet, reality in SWAPS:
        # TRUE вариант + перефразировка
        out.append(make_ex_true(true_claim, rnd_source(), snippet,
                                dv=random.randint(85, 96),
                                cv=random.randint(85, 94)))
        # FALSE вариант
        out.append(make_ex_false(false_claim, rnd_source(), snippet, reality,
                                 dv=random.randint(3, 12),
                                 cv=random.randint(82, 92)))
    # Если меньше 100 — добавим перефразировки
    while len(out) < 100:
        true_c, false_c, snip, real = random.choice(SWAPS)
        out.append(make_ex_false(false_c, rnd_source(), snip, real,
                                 dv=random.randint(3, 12),
                                 cv=random.randint(82, 92)))
    return out[:100]


def gen_quantifiers() -> list:
    """100 примеров: 50 TRUE + 50 FALSE (дублируем с перефразировками)."""
    out = []
    # TRUE (10 баз × 5 формулировок)
    for claim, snippet, cv_base in QUANTIFIERS_TRUE:
        variations = [
            claim,
            claim.replace("Более", "Свыше").replace("Менее", "Менее чем"),
            claim.replace("Около", "Приблизительно").replace("около", "приблизительно"),
            f"По статистике, {claim[0].lower() + claim[1:]}",
            f"Данные показывают: {claim[0].lower() + claim[1:]}",
        ]
        for v in variations:
            out.append(make_ex_true(v, rnd_source(), snippet,
                                    dv=random.randint(82, 95),
                                    cv=random.randint(min(78, cv_base - 5), max(cv_base, 82))))
    # FALSE (10 баз × 5 формулировок)
    for claim, snippet, reality in QUANTIFIERS_FALSE:
        variations = [
            claim,
            claim.replace("Более", "Свыше").replace("Менее", "Менее чем"),
            claim.replace("Около", "Примерно"),
            f"Считается, что {claim[0].lower() + claim[1:]}",
            f"Широко известно: {claim[0].lower() + claim[1:]}",
        ]
        for v in variations:
            out.append(make_ex_false(v, rnd_source(), snippet, reality,
                                     dv=random.randint(3, 10),
                                     cv=random.randint(84, 92)))
    return out[:100]


def gen_directional() -> list:
    """100 примеров: 25 пар × (TRUE+FALSE) × 2 перефразировки."""
    out = []
    for true_c, false_c, snippet, reality in DIRECTIONAL:
        out.append(make_ex_true(true_c, rnd_source(), snippet,
                                dv=random.randint(88, 97),
                                cv=random.randint(85, 94)))
        out.append(make_ex_false(false_c, rnd_source(), snippet, reality,
                                 dv=random.randint(3, 10),
                                 cv=random.randint(84, 93)))
    while len(out) < 100:
        tc, fc, snip, real = random.choice(DIRECTIONAL)
        out.append(make_ex_false(fc, rnd_source(), snip, real,
                                 dv=random.randint(3, 10),
                                 cv=random.randint(84, 93)))
    return out[:100]


