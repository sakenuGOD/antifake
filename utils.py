def check_locations_in_sources(
    claim_locations: List[str],
    raw_results: List[Dict],
    max_sources: int = 10,
) -> Dict[str, any]:
    """Проверяет наличие локаций из claim в источниках.

    Returns:
        {
            "confirmed": ["Париж"],
            "missing": ["Токио"],
            "all_confirmed": True/False,
            "all_missing": True/False,
        }
    """
    if not claim_locations or not raw_results:
        return {
            "confirmed": [], "missing": [],
            "all_confirmed": False, "all_missing": False,
        }

    src_text = " ".join(
        (r.get("snippet", "") + " " + r.get("title", "")).lower()
        for r in raw_results[:max_sources]
    )

    confirmed = []
    missing = []
    for loc in claim_locations:
        ll = loc.lower()
        found = ll in src_text or any(
            t in src_text for t in TRANSLITERATION_MAP.get(ll, [])
        )
        (confirmed if found else missing).append(loc)

    return {
        "confirmed": confirmed,
        "missing": missing,
        "all_confirmed": bool(confirmed) and not missing,
        "all_missing": bool(missing) and not confirmed,
    }


def location_found_in_text(
    loc_lower: str,
    text: str,
) -> bool:
    """Проверяет одну локацию (с транслитерацией) в тексте."""
    if loc_lower in text:
        return True
    return any(t in text for t in TRANSLITERATION_MAP.get(loc_lower, []))
