SELF_CRITIQUE_TEMPLATE = """\
[INST]Ты — критик, проверяющий работу аналитика по фактчекингу. Найди ошибки в его анализе.

УТВЕРЖДЕНИЕ: {claim}

ИСТОЧНИКИ:
{search_results}

АНАЛИЗ АНАЛИТИКА:
{verdict}

Проверь по пунктам:
1. РЕЛЕВАНТНОСТЬ: Аналитик использовал источники, которые описывают ТО ЖЕ событие? Или спутал похожие слова с подтверждением?
2. ЦИФРЫ: Если есть числа в утверждении — совпадают ли они с числами в источниках?
3. ЛОГИКА: Вердикт и score согласованы? (ПРАВДА=70-100, ЛОЖЬ=0-29, НЕ ПОДТВЕРЖДЕНО=30-69)
4. ОСТОРОЖНОСТЬ: Не слишком ли уверенный вердикт при слабых доказательствах?

Ответ строго в формате:
ОШИБКИ: [перечисли найденные ошибки через точку с запятой, или "нет"]
КОРРЕКЦИЯ: [ДА / НЕТ]
РЕКОМЕНДУЕМЫЙ_SCORE: [число 0-100]
РЕКОМЕНДУЕМЫЙ_ВЕРДИКТ: [ПРАВДА / ЛОЖЬ / НЕ ПОДТВЕРЖДЕНО][/INST]"""
