CREDIBILITY_ASSESSMENT_TEMPLATE = """\
[INST]Ты — эксперт по проверке фактов с многолетним опытом. Определи достоверность утверждения на основе найденных источников.

УТВЕРЖДЕНИЕ: {claim}

РЕЗУЛЬТАТЫ ПОИСКА:
{search_results}

{search_hint}

Рассуждай строго по шагам:

ШАГ 1 — ИДЕНТИФИКАЦИЯ СОБЫТИЯ:
Какое конкретно событие описывает утверждение? (кто, что, где, когда, какие числа)
Для каждого источника определи: описывает ли он ТОЧНО ТО ЖЕ событие с ТЕМИ ЖЕ деталями?
ВНИМАНИЕ: совпадение отдельных слов НЕ означает подтверждение!
Пример: "Путин подписал указ о назначении судей" НЕ подтверждает "Путин подписал указ о запрете крипты" — это РАЗНЫЕ указы.
Выпиши только те источники, которые описывают ТО ЖЕ САМОЕ событие.

ШАГ 2 — СРАВНЕНИЕ ФАКТОВ:
Из РЕЛЕВАНТНЫХ источников (Шаг 1):
- Совпадающие факты: "[Источник] сообщает: ..."
- Противоречащие факты: "[Источник] утверждает обратное: ..."
Если ни один источник не описывает то же событие — напиши "Ни один источник не подтверждает это конкретное событие."

ШАГ 3 — ЧИСЛОВАЯ И ДАТОВАЯ ПРОВЕРКА (КРИТИЧНО!):
Если в утверждении есть числа (%, даты, суммы, количество):
- Выпиши ВСЕ числа из утверждения: проценты, суммы, даты, количества.
- Выпиши ВСЕ числа из источников рядом с каждым.
- Сравни каждое число ПОШТУЧНО. Расхождение более 10% = несоответствие.
- ДАТЫ: проверь год, месяц, день. Событие в 2020 ≠ событие в 2016. Разница даже в 1 год = ошибка.
- ГЕОГРАФИЯ: проверь место. Олимпиада в Париже ≠ Олимпиада в Токио.
- ПЕРСОНЫ: проверь кто именно. Директор Tesla ≠ директор Meta.
{verification_hints}

ШАГ 4 — ТРОЙНАЯ САМОПРОВЕРКА + АДВОКАТ ДЬЯВОЛА:
А) Не путаю ли я "похожие слова" с "то же событие"? Источник про другой указ/закон/событие НЕ является подтверждением.
Б) Не путаю ли я "не найдено подтверждение" с "опровергнуто"? Это РАЗНЫЕ вещи.
В) АДВОКАТ ДЬЯВОЛА: какие аргументы ПРОТИВ моего вердикта? Что могло бы изменить моё мнение? Если аргументы против сильные — пересмотри вердикт.
Г) Если бы я узнал правду через неделю — согласился бы со своим вердиктом?

ПРАВИЛА ВЕРДИКТА:
• ПРАВДА (70-100): источник описывает ТОЧНО ТО ЖЕ событие/факт и подтверждает его. Укажи какой.
  ТАКЖЕ ПРАВДА если утверждение — ОБЩЕИЗВЕСТНЫЙ ФАКТ (география, наука, история, крупные организации) и ты уверен в его истинности из своих знаний, а источники косвенно подтверждают (упоминают те же сущности в правильном контексте).
• ЛОЖЬ (0-29): источники ПРЯМО ОПРОВЕРГАЮТ (утверждают обратное с другими числами/датами/фактами) ИЛИ событие физически невозможно ИЛИ ты ТОЧНО знаешь что факт ложный.
• НЕ ПОДТВЕРЖДЕНО (30-69): источники не найдены, описывают ДРУГИЕ события, или данных недостаточно.

ВАЖНО — ОБЩЕИЗВЕСТНЫЕ ФАКТЫ:
Если утверждение описывает общепризнанный факт (столица страны, крупная организация, научный факт, известная личность), и ты УВЕРЕН в его правильности — ставь ПРАВДА (80-95), даже если источники подтверждают лишь косвенно.
Примеры общеизвестных фактов: "Москва — столица России", "Байкал — самое глубокое озеро", "Google разработала Android".

ЗАПРЕТЫ:
• Нельзя ставить ПРАВДА если источники описывают ДРУГОЕ событие с похожими словами (для СОБЫТИЙ и НОВОСТЕЙ).
• Нельзя ставить ЛОЖЬ если подтверждение просто не найдено — это НЕ ПОДТВЕРЖДЕНО.
• Нельзя ставить НЕ ПОДТВЕРЖДЕНО для общеизвестных фактов которые ты точно знаешь.

Ответ строго в формате:
ДОСТОВЕРНОСТЬ: [число от 0 до 100]
ВЕРДИКТ: [ПРАВДА / ЛОЖЬ / НЕ ПОДТВЕРЖДЕНО]
УВЕРЕННОСТЬ: [число от 0 до 100]
ОБОСНОВАНИЕ: [3-5 предложений. ОБЯЗАТЕЛЬНО укажи конкретные числа/даты из утверждения и источников — что совпало, а что нет]
ИСТОЧНИКИ: [список через точку с запятой][/INST]"""


