CREDIBILITY_ASSESSMENT_TEMPLATE = """\
[INST]Ты — эксперт по проверке фактов. Определи достоверность утверждения.

УТВЕРЖДЕНИЕ: {claim}

РЕЗУЛЬТАТЫ ПОИСКА:
{search_results}

{search_hint}

{nli_hint}

{number_comparison}

Рассуждай строго по шагам:

ШАГ 1 — ИДЕНТИФИКАЦИЯ:
Какое конкретно событие описывает утверждение? (кто, что, где, когда, какие числа)
Для каждого источника: описывает ли он ТОЧНО ТО ЖЕ событие с ТЕМИ ЖЕ деталями?
ВНИМАНИЕ: совпадение отдельных слов НЕ означает подтверждение!

ШАГ 2 — ФАКТЫ:
Из релевантных источников:
- Совпадающие факты: "[Источник] сообщает: ..."
- Противоречащие факты: "[Источник] утверждает обратное: ..."
Если ни один источник не описывает то же событие — напиши это.
{verification_hints}

ШАГ 3 — ВЫВОД:
NLI и числовые данные проверены автоматически. Учитывай их как факт.
Если NLI показывает contradiction — объясни почему утверждение ложно.
Если NLI показывает entailment — объясни что подтверждено.
Если числа расходятся — это расхождение с реальностью.

ПРАВИЛА ВЕРДИКТА:
- ПРАВДА (70-100): источники подтверждают ТО ЖЕ событие/факт. Или это общеизвестный факт.
- ЛОЖЬ (0-29): источники ПРЯМО ОПРОВЕРГАЮТ (другие числа/даты/факты), или событие невозможно.
- НЕ ПОДТВЕРЖДЕНО (30-69): источники не найдены, описывают ДРУГИЕ события, или данных недостаточно.

Ответ строго в формате:
ДОСТОВЕРНОСТЬ: [число от 0 до 100]
ВЕРДИКТ: [ПРАВДА / ЛОЖЬ / НЕ ПОДТВЕРЖДЕНО]
УВЕРЕННОСТЬ: [число от 0 до 100]
ОБОСНОВАНИЕ: [3-5 предложений с конкретными числами/датами из утверждения и источников]
ИСТОЧНИКИ: [список через точку с запятой][/INST]"""


