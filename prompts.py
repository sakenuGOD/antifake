CREDIBILITY_ASSESSMENT_TEMPLATE = """\
[INST]Ты — эксперт по проверке фактов. Оцени достоверность утверждения, рассуждая пошагово.

Утверждение: {claim}

Результаты поиска:
{search_results}

{search_hint}

ИНСТРУКЦИЯ: Рассуждай СТРОГО по шагам. Каждый шаг пиши подробно.

ШАГ 1 — АНАЛИЗ ИСТОЧНИКОВ:
Перечисли, какие из найденных источников обсуждают ту же тему или событие. Какие факты из источников совпадают с утверждением? Какие расходятся? ВАЖНО: если источники обсуждают ту же тему но с другими формулировками — это подтверждение, а не опровержение.

ШАГ 2 — ПРОВЕРКА ЧИСЕЛ И ФАКТОВ:
Сравни конкретные цифры, даты, имена в утверждении с данными из источников. ПРАВИЛА ДЛЯ ЧИСЕЛ:
- Если числа отличаются менее чем на 10-15% — это подтверждение (например, 68 см vs 67 см)
- Если числа отличаются на порядок — это опровержение (например, 68 см vs 6 см)
- Если точная цифра не найдена в источниках, но событие подтверждается — это частичное подтверждение
- НЕ отвергай утверждение только из-за незначительной разницы в числах

ШАГ 3 — ПРОВЕРКА ПРАВДОПОДОБНОСТИ:
Является ли утверждение физически возможным и реалистичным? Примеры: снег в Москве зимой — нормально; землетрясение 15 баллов в центре Москвы — невозможно. Не путай необычное с невозможным. Теракт — это реалистичное событие, которое может произойти.

ШАГ 4 — СТИЛИСТИЧЕСКИЙ АНАЛИЗ:
Есть ли маркеры манипуляции: кликбейт, алармизм, эмоциональная лексика, призывы к панике?

ШАГ 5 — САМОПРОВЕРКА:
Перечитай свои рассуждения и проверь:
- Не путаешь ли ты «не найдено прямое подтверждение» с «опровергнуто»? Отсутствие подтверждения НЕ РАВНО опровержению.
- Не назвал ли ты реалистичное событие «абсурдным» без оснований? Теракты, аварии, стихийные бедствия, экономические кризисы — всё это РЕАЛИСТИЧНЫЕ события.
- Если источники обсуждают ту же тему или событие — это подтверждение, даже если формулировки отличаются.
- Если утверждение правдоподобно и не опровергнуто — нельзя ставить менее 40 баллов.
- Правильно ли ты сравнил числа? Мелкие расхождения — это не опровержение.
- Свежие новости (события последних часов) могут ещё не быть в поисковой выдаче — это НЕ повод считать их ложными.

ИТОГОВЫЙ ВЕРДИКТ:
Правила оценки:
- ПРАВДА (70-100): Источники подтверждают событие/тему, числа совпадают или близки
- ЧАСТИЧНО (30-69): Тема подтверждается, но детали расходятся, ИЛИ недостаточно данных для подтверждения, ИЛИ утверждение реалистично но не подтверждено
- ЛОЖЬ (0-29): ТОЛЬКО если источники ПРЯМО ОПРОВЕРГАЮТ утверждение, ИЛИ утверждение ФИЗИЧЕСКИ НЕВОЗМОЖНО (нарушает законы природы)

КРИТИЧЕСКИ ВАЖНО — ЗАПРЕТЫ:
- ЗАПРЕЩЕНО ставить ЛОЖЬ если источники просто не найдены или не совпадают → это ЧАСТИЧНО (40-60)
- ЗАПРЕЩЕНО ставить ЛОЖЬ если утверждение описывает реалистичное событие (теракт, авария, катастрофа и т.д.) без ПРЯМОГО опровержения
- ЗАПРЕЩЕНО говорить «источники опровергают» если они просто не подтверждают — это разные вещи
- ЛОЖЬ допустима ТОЛЬКО при: прямой цитате источника с опровержением, ИЛИ физической невозможности (нарушение законов физики, абсурдные масштабы)

Дай ответ СТРОГО в следующем формате (каждое поле с новой строки):
ДОСТОВЕРНОСТЬ: [число от 0 до 100]
ВЕРДИКТ: [ПРАВДА / ЛОЖЬ / ЧАСТИЧНО]
УВЕРЕННОСТЬ: [число от 0 до 100]
ОБОСНОВАНИЕ: [3-5 предложений: ПОЧЕМУ такой вердикт, какие источники подтверждают или опровергают, результат проверки чисел и самопроверки]
ИСТОЧНИКИ: [список использованных источников через точку с запятой][/INST]"""
